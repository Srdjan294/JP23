package klase;

public class Evidencija_oznaka {
	
	private int evidencija;
	private int oznaka;
	
	//getteri i setteri
	public int getEvidencija() {
		return evidencija;
	}
	public void setEvidencija(int evidencija) {
		this.evidencija = evidencija;
	}
	public int getOznaka() {
		return oznaka;
	}
	public void setOznaka(int oznaka) {
		this.oznaka = oznaka;
	}
	
	//konstruktor
	public Evidencija_oznaka(int evidencija, int oznaka) {
		super();
		this.evidencija = evidencija;
		this.oznaka = oznaka;
	}
	
	
	
	

}
